% 

f=[1.0769   0.15634   0.003407   0.000031;...%1s_sigma_g
   1.0221   0.04448   0.000296   0.000001;...%2s_sigma_g
   1.0105   0.02094   0.000067   0.0;...     %3s_sigma_g
   1.0368   0.02474   0.000168   0.000001;...%2p_sigma_u    
